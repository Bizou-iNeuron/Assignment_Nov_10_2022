
public class SumArray {
// calculate the sum of an array
	// use of for loop, compound assignment
	public static void main(String[] args) {
		int [] a = {23,12,523,83};
		int s = 0;
		for (int i =0 ; i< a.length;i++)
		{
			
			s += a[i];
		}
		System.out.println("som of array is "+s);
	}
}
